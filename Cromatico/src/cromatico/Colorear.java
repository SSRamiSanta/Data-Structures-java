package cromatico;

public class Colorear {
	int numero;
	int color;
	
	public Colorear(int numero, int color) {
		this.numero = numero;
		this.color = color;
	}

	public int getNumero() {
		return numero;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}
	
}
